-- ============================================================
-- Project    : bariLMS — Learning Management System (SENA)
-- File       : database/bari_lms_postgresql.sql
-- Description: Canonical PostgreSQL schema for bariLMS.
--              Supersedes database/setup_aprendiz.sql (legacy).
--
-- PostgreSQL : Requires PostgreSQL 17+
--
-- How to apply:
--   1. psql -U <user> -d <dbname> -f database/bari_lms_postgresql.sql
--   2. psql -U <user> -d <dbname> -f database/seed.sql
--   (safe to re-run; all DDL uses IF NOT EXISTS)
--
-- ── UUID ────────────────────────────────────────────────────
--   UUID v7 generated by the application layer.
--   No DEFAULT on id columns — the app always provides them.
--   sesion.id doubles as the jti claim for BOTH the access token
--   and the refresh token (same UUID v7). Invalidating the session
--   row revokes both tokens simultaneously.
--
-- ── actualizado_en ──────────────────────────────────────────
--   NULL until the first UPDATE. Set by the app on every UPDATE.
--
-- ── creado_por ──────────────────────────────────────────────
--   NULL for the provisional admin and catalog rows.
--   ON DELETE SET NULL — audit rows are never orphaned.
--
-- ── COLLATE "C" ─────────────────────────────────────────────
--   Applied to codes, hashes, emails and document numbers
--   (ASCII / lowercase). Gives byte-order index scans (faster,
--   locale-free). Spanish free-text columns use the DB default
--   collation for correct locale sort.
--
-- ── Case rules (enforced by CHECK) ──────────────────────────
--   correo_institucional, correo_personal → always lower().
--   usuario.nombre, persona.nombres, persona.apellidos → upper().
--
-- ── Persons & accounts ──────────────────────────────────────
--   usuario   — authentication anchor (credentials, display name).
--               The provisional admin lives here without a persona row.
--   persona   — personal/identity data for real people.
--               persona.usuario_id NOT NULL UNIQUE: always linked to a
--               user account; one account = one persona.
--               correo_personal lives here (not in usuario).
--   instructor, aprendiz, personal_administrativo — role-specific data.
--               Each links to persona (persona_id NOT NULL UNIQUE).
--               No direct usuario_id: reach the account via persona.
--
-- ── Dashboard routing (app layer) ───────────────────────────
--   /dashboard → single profile: redirect to perfil.ruta_dashboard.
--                multiple profiles: show selector first.
--   /dashboard/<slug> → blocked unless usuario_perfil links the
--   authenticated user to that perfil.
--
-- ── JWT / cookies (app layer) ───────────────────────────────
--   Cookies: HttpOnly, Secure, SameSite=Strict.
--   access_token  : 15 min, JWT typ=access, NOT stored in DB.
--   refresh_token : 1 day,  JWT typ=refresh, Argon2 hash in sesion.
--   Both tokens carry the same jti = sesion.id (UUID v7).
--   Validation (access):  sig → typ → exp → sesion activo.
--   Validation (refresh): sig → typ → exp → sesion activo → Argon2.
--   Hash mismatch on refresh → revoke with tipo=token_reutilizado.
--   On success: rotate hash, issue new access token.
--
-- ── Provisional admin (app layer) ───────────────────────────
--   On startup with no Administrador user: create one usuario row
--   with random correo_institucional + random password, print to
--   stdout once. No persona row is created for it.
--
-- ── Tables (dependency order) ───────────────────────────────
--   Catalogs : tipo_documento, tipo_invalidacion_sesion
--   Auth     : usuario, persona, sesion, sesion_historial
--   Access   : perfil, rol, permiso, rol_permiso,
--              usuario_perfil, usuario_rol
--   Structure: regional, centro, coordinacion, sede, ambiente
--   Knowledge: red_conocimiento, area, nivel_formacion
--   People   : instructor, aprendiz, personal_administrativo
--   Content  : proyecto_formativo, fase_proyecto, actividad_proyecto,
--              actividad_aprendizaje, guia_aprendizaje,
--              evidencia_aprendizaje, entrega_evidencia,
--              seccion_actividad, sub_seccion_actividad,
--              guia_actividad_proyecto
--   Programs : programa_formacion, ficha_formacion,
--              ficha_instructor_competencia, asistencia_aprendiz
--   Extras   : fase, notificacion
-- ============================================================

BEGIN;

-- ============================================================
-- CATALOGS
