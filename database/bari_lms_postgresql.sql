-- ============================================================
-- PEOPLE
-- ============================================================
-- Cada tabla de persona lleva persona_id NOT NULL UNIQUE.
-- persona.usuario_id lleva al usuario con las credenciales.
-- No se repite usuario_id aquí: se accede vía persona.
